#------------------------------------------------------------
# ErrorOutput takes a text
# return: Print the message and aborts script
#         


ErrorOutput <- function(errorMessage="Unidentified error"){
  
  
  cat(" \n\n\n @@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@", sep="")
  cat(" \n @@ !-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!", sep="")
  cat(" \n @@ ", sep="")
  cat(" \n @@\t Error", sep="")
  cat(" \n @@ ", sep="")
  cat(" \n @@\t ", errorMessage, sep="")
  cat(" \n @@ ", sep="")
  cat(" \n @@ !-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!-!", sep="")
  cat(" \n @@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@", sep="")
  
  # Print sessionInfo()
  cat(" \n\n\n|| -+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-   ", sep="")
  cat(" \n \n", sep="")
  print(sessionInfo())
  cat(" \n", sep="")
  cat(" \n|| -+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+- \n\n  ", sep="")
  
  
  # Abort script
  quit(save = "no", status = 0, runLast = TRUE)
  #stop()
}
